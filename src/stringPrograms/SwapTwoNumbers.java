package stringPrograms;

public class SwapTwoNumbers {
	
	public static void main(String[] args) {
		
		int a = 100;
		int b = 200;
		int temp =0;
		System.out.println("a= " + a + "and" + "b= " + b);
		temp = b;
		b=a;
		a=temp;
		System.out.println("a= " + a +"and" + "b= " + b);
	}

}
